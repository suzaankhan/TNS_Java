package com.suzaan.assignment.employees;

public class Employee {
    private String name;
    private int salary;
    private int empId;

    public String getName() {
        return name;
    }
    public int getSalary() {
        return salary;
    }
    public int getEmpId() {
        return empId;
    }
    public void setName(String name) {
        this.name = name;
    }
    public void setSalary(int salary) {
        this.salary = salary;
    }
    public void setEmpId(int empId) {
        this.empId = empId;
    }
}
